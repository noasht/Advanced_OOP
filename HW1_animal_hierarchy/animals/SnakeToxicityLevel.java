package animals;

/**
 * The SnakeToxicityLevel enumeration represents different toxicity levels of
 * snakes. It includes NON for non-toxic snakes, POISONOUS for poisonous snakes,
 * and LETHAL for lethal snakes.
 */
public enum SnakeToxicityLevel {
	/**
	 * Represents a non-toxic snake.
	 */
	NON,
	/**
	 * Represents a poisonous snake.
	 */
	POISONOUS,
	 /**
     * Represents a lethal snake.
     */
	LETHAL
}
