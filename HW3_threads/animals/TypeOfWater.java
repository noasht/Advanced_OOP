package animals;
/**
 * The TypeOfWater enumeration represents different types of water environments.
 * This enum defines two types of water environments: SEA for saltwater environments
 * and SWEET for freshwater environments.
 */

public enum TypeOfWater {
	 /**
     * Represents a saltwater environment, such as oceans and seas.
     */
	SEA,

    /**
     * Represents a freshwater environment, such as lakes and rivers.
     */
	SWEET
}
