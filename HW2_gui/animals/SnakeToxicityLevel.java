package animals;
/**
 * The SnakeToxicityLevel enumeration represents different toxicity levels of snakes.
 * This enum defines three levels of toxicity: LOW, MEDIUM, and HIGH.
 */
public enum SnakeToxicityLevel {
	  /**
     * Represents a low level of toxicity.
     */
	LOW,
	/**
     * Represents a medium level of toxicity.
     */
	MEDIUM,
	/**
     * Represents a high level of toxicity.
     */
	HIGH
}
